package org.yurovnik.germes.app.model.entity.geography;

/**
 * Geographical coordinate of an object
 * @author YurovnikMV
 */
public class Coordinate {
    private double x;
    private double y;

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
}
